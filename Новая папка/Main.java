import java.util.Arrays;
import java.util.List;

public class Main {
    public static void main(String[] args) {
        // Создание учебных групп
        УчебнаяГруппа группа1 = new УчебнаяГруппа("Группа 1");
        УчебнаяГруппа группа2 = new УчебнаяГруппа("Группа 2");
        УчебнаяГруппа группа3 = new УчебнаяГруппа("Группа 3");

        // Создание потоков
        Поток поток1 = new Поток(Arrays.asList(группа1, группа2));
        Поток поток2 = new Поток(Arrays.asList(группа1));
        Поток поток3 = new Поток(Arrays.asList(группа1, группа2, группа3));

        // Список потоков
        List<Поток> потоки = Arrays.asList(поток1, поток2, поток3);

        // Создание сервиса и контроллера
        ПотокСервис потокСервис = new ПотокСервис();
        Контроллер контроллер = new Контроллер(потокСервис);

        // Сортировка потоков
        контроллер.сортироватьПотоки(потоки);

        // Вывод отсортированных потоков
        for (Поток поток : потоки) {
            System.out.println("Поток с " + поток.getГруппы().size() + " группами");
        }
    }
}
